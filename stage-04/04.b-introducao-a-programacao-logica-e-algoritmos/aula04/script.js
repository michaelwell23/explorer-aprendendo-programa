let firstNumber = Number(prompt('Digite o primeiro numero'));
let secondNumber = Number(prompt('Digite o primeiro numero'));

const sum = firstNumber + secondNumber;
const subtration = firstNumber - secondNumber;
const multiplication = firstNumber * secondNumber;
const division = firstNumber / secondNumber;
const moduleOperation = firstNumber % secondNumber;

alert(`Soma: ${sum}`);
alert(`Subtração:  ${subtration}`);
alert(`Multiplicação ${multiplication}`);
alert(`Divisão ${division}`);
alert(`Módulo ${moduleOperation}`);
